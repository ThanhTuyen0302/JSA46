//1. Toan tu so hoc (Arithmetic) (+-*/%)
var a = 12 **2; // Luy thua
console.log(a)
// *** 1. tien to (prefix) (++a)
console.log(++a); // 145
console.log(a); // 145

// *** 1. hau to (postfix) (a++)
console.log(a++); // 144
console.log(a); //145

console.log(12 % 2); // chia Lay phan du
console.log(12 / 2); // 6.5

